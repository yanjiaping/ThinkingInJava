package chapter3.example;

/**
 * 自动递增和递减
 * 
 * @author YJP
 *
 */
public class AutoInc3_6 {

	public static void main(String[] args) {
		int i = 1;
		System.out.println("i：" + i);
		System.out.println("++i：" + ++i);
		System.out.println("i++：" + i++);
		System.out.println("i：" + i);
		System.out.println("--i：" + --i);
		System.out.println("i--：" + i--);
		System.out.println("i：" + i);
	}

}
